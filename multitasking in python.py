#multitasking :
#executing multiple task at the same time.


#multitasking are of two types :
#process based multitasking
#thread based multitasking


#process based multitasking :
#executing multiple task at the same time where each task is a separate independent program(process), is called process based multitasking. It is suitable for operating system level.

#thread based multitasking :
#executing multiple task at the same time where each task is a seperate independent part of the same program(process ), is called thread based multitasking and each independent part is called thread. It is suitable for programmatic level.
